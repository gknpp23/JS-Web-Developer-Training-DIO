// Declarar variáveis 
// let (variável) e const(constante)

// Para exemplificar, vamos calcular a área de um circulo
let variavel_raio = 10;
const PI = 3.14;

// Operadores aritmeticos -> soma, subtração, divisão e multiplicação
// (+),(-),(*),(/)

// formula: PI X Raio^2
let variavel_area = PI * (variavel_raio * variavel_raio);

// Saídas no console
console.log(variavel_area);




